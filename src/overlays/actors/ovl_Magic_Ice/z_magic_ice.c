void MagicIce_Init(Actor* thisx, GlobalContext* globalCtx) {
    MagicIce* this = THIS;

    this->action = 0;
    this->actionTimer = 0;
    Collider_InitCylinder(globalCtx, &this->collider);
    Collider_SetCylinder(globalCtx, &this->collider, &this->actor, &sCylinderInit);
    Collider_UpdateCylinder(thisx, &this->collider);
    this->actor.update = &MagicIce_UpdateBeforeCast;
    this->actionTimer = 20;
    this->actor.room = -1;

    this->colliderScale.x = this->colliderScale.y = this->colliderScale.z = 0.0f;
    SkelCurve_Init(globalCtx, &this->skelCurve, &sLimbList, &sTransformUpdIdx);
    SkelCurve_SetAnim(&this->skelCurve, &sTransformUpdIdx, 0.0f, 10000.0f, 0.0f, 1.0f);
    this->alphaMultiplier = 1.0f;
}

void MagicIce_Destroy(Actor* thisx, GlobalContext* globalCtx) {
    MagicIce* this = THIS;

    func_800876C8(globalCtx);

    SkelCurve_Destroy(globalCtx, &this->skelCurve);
}

void MagicIce_UpdateBeforeCast(Actor* thisx, GlobalContext* globalCtx) {
    MagicIce* this = THIS;
    Player* player = PLAYER;

    if ((globalCtx->msgCtx.msgMode == 0xD) || (globalCtx->msgCtx.msgMode == 0x11)) {
        Actor_Kill(&this->actor);
        return;
    }
    if (this->actionTimer > 0) {
        this->actionTimer--;
    } else {
        this->actor.update = &MagicIce_Update;
    }
    this->actor.world.pos = player->actor.world.pos;
}

void MagicIce_Update(Actor* thisx, GlobalContext* globalCtx) {
    MagicIce* this = THIS;
    Player* player = PLAYER;
    s32 pad;

    this->actor.world.pos = player->actor.world.pos;
    if ((globalCtx->msgCtx.msgMode == 0xD) || (globalCtx->msgCtx.msgMode == 0x11)) {
        Actor_Kill(&this->actor);
        return;
    }
    if (this->action == ACTION_GROW_SLOWLY) {
        this->collider.info.toucher.damage = this->actionTimer + 25;
    } else if (this->action == ACTION_STOP_GROWING) {
        this->collider.info.toucher.damage = this->actionTimer;
    }
    Collider_UpdateCylinder(&this->actor, &this->collider);
    this->collider.dim.radius = (this->colliderScale.x * 325.0f);
    this->collider.dim.height = (this->colliderScale.y * 450.0f);
    this->collider.dim.yShift = (this->colliderScale.y * -225.0f);
    CollisionCheck_SetAT(globalCtx, &globalCtx->colChkCtx, &this->collider.base);

    switch (this->action) {
        case ACTION_INITIALIZE:
            this->actionTimer = 30;
            this->colliderScale.x = this->colliderScale.y = this->colliderScale.z = 0.0f;
            this->actor.world.rot = player->actor.world.rot;
            this->actor.shape.rot = player->actor.world.rot;
            this->scalingSpeed = 0.08f;
            this->action++;
            break;
        case ACTION_GROW_SLOWLY: // Ice sphere slowly grows out of player for 30 frames
            if (this->actionTimer > 0) {
                Math_SmoothStepToF(&this->colliderScale.x, 0.4f, this->scalingSpeed, 0.1f, 0.001f);
                this->colliderScale.y = this->colliderScale.z = this->colliderScale.x;
            } else {
                this->actionTimer = 25;
                this->action++;
            }
            break;
        case ACTION_STOP_GROWING: // Sphere stops growing and maintains size for 25 frames
            if (this->actionTimer <= 0) {
                this->actionTimer = 15;
                this->action++;
                this->scalingSpeed = 0.05f;
            }
            break;
        case ACTION_GROW_QUICKLY: // Sphere beings to grow again and quickly expands out until killed
            this->alphaMultiplier -= 0.06722689f;
            this->colliderScale.x += this->scalingSpeed;
            this->colliderScale.y += this->scalingSpeed;
            this->colliderScale.z += this->scalingSpeed;
            thisx->scale.x += this->scalingSpeed * 0.025;
            thisx->scale.y += this->scalingSpeed * 0.025;
            thisx->scale.z += this->scalingSpeed * 0.025;
            if (this->alphaMultiplier <= 0.0f) {
                this->action = 0;
                Actor_Kill(&this->actor);
            }
            break;
    }
    if (this->actionTimer > 0) {
        this->actionTimer--;
    }

    SkelCurve_Update(globalCtx, &this->skelCurve);
    if (this->actionTimer > 15) {
        Audio_PlayActorSound2(&this->actor, NA_SE_IT_EXPLOSION_ICE - SFX_FLAG);
    }
    Audio_PlayActorSound2(&this->actor, NA_SE_EV_ICE_FREEZE - SFX_FLAG);
}

