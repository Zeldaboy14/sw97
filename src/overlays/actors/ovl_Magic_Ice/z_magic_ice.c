void MagicIce_Draw(Actor* thisx, GlobalContext* globalCtx) {
    GraphicsContext* gfxCtx = globalCtx->state.gfxCtx;
    MagicIce* this = THIS;
    s32 i;
    u8 alpha;

    if (this->action > 0) {
        OPEN_DISPS(gfxCtx);

        POLY_XLU_DISP = Gfx_CallSetupDL(POLY_XLU_DISP, 25);
        SkelCurve_Draw(thisx, globalCtx, &this->skelCurve, MagicIce_OverrideLimbDraw, NULL, 1, NULL);

        CLOSE_DISPS(gfxCtx);

        alpha = (s32)(this->alphaMultiplier * 255);
        for (i = 0; i < ARRAY_COUNT(sCylinderVtx); i++) {
            if (sCylinderVtx[i].n.a > 0) {
                sCylinderVtx[i].n.a = alpha;
            }
        }
    }
}
